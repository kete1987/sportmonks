package es.com.kete1987.sportmonks.library.core.model.continent;

public class Continent {
    private Long id;
    private String name;
    private String code;
    private String flag;
    private String image_path;

    public Continent() {
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }

    public String getFlag() {
        return flag;
    }

    public String getImagePath() {
        return image_path;
    }
}
