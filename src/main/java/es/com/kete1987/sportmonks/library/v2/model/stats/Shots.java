package es.com.kete1987.sportmonks.library.v2.model.stats;

public class Shots {
    private final Integer total = 0;
    private final Integer ongoal = 0;
    private final Integer offgoal = 0;
    private final Integer blocked = 0;
    private final Integer insidebox = 0;
    private final Integer outsidebox = 0;

    public Shots() {
    }

    public Integer getTotal() {
        return total;
    }

    public Integer getOngoal() {
        return ongoal;
    }

    public Integer getOffgoal() {
        return offgoal;
    }

    public Integer getBlocked() {
        return blocked;
    }

    public Integer getInsidebox() {
        return insidebox;
    }

    public Integer getOutsidebox() {
        return outsidebox;
    }
}
