package es.com.kete1987.sportmonks.library.v2.model.standings;

public class StandingInfo {
    private final int games_played = 0;
    private final int won = 0;
    private final int draw = 0;
    private final int lost = 0;
    private final int goals_scored = 0;
    private final int goals_against = 0;

    public StandingInfo() {
    }

    public int getGamesPlayed() {
        return games_played;
    }

    public int getWon() {
        return won;
    }

    public int getDraw() {
        return draw;
    }

    public int getLost() {
        return lost;
    }

    public int getGoalsScored() {
        return goals_scored;
    }

    public int getGoalsAgainst() {
        return goals_against;
    }
}
