package es.com.kete1987.sportmonks.library.core.model.region;

public class Region {
    private Long id;
    private Long country_id;
    private String name;

    public Region() {
    }

    public Long getId() {
        return id;
    }

    public Long getCountryId() {
        return country_id;
    }

    public String getName() {
        return name;
    }
}
